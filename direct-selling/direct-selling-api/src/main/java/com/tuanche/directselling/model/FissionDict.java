package com.tuanche.directselling.model;

import java.io.Serializable;

public class FissionDict implements Serializable {

    private static final long serialVersionUID = -2692477939016695840L;
    private Integer id;

    private Short type;

    private String code;

    private String name;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Short getType() {
        return type;
    }

    public void setType(Short type) {
        this.type = type;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}